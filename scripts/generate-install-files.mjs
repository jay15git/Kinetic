#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..")
const registry = JSON.parse(readFileSync(join(ROOT, "registry.json"), "utf-8"))
const item = registry.items.find((entry) => entry.name === "scrub-number-field")

if (!item) {
  throw new Error("scrub-number-field registry item not found")
}

const contents = Object.fromEntries(
  item.files.map((file) => [
    file.path,
    readFileSync(join(ROOT, file.path), "utf-8"),
  ]),
)

const output = `// Generated by scripts/generate-install-files.mjs — do not edit manually.

export const SCRUB_INSTALL_REGISTRY_FILE_CONTENTS: Record<string, string> = ${JSON.stringify(contents, null, 2)}
`

writeFileSync(
  join(ROOT, "lib/scrub-install-registry-files.generated.ts"),
  output,
)

console.log(
  `Wrote lib/scrub-install-registry-files.generated.ts (${item.files.length} files)`,
)
